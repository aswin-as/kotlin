package LessonOneVariables

fun main() {
    //INT
    val maxIntValue : Int = Int.MAX_VALUE
    val minIntValue: Int = Int.MIN_VALUE

    println("Maximum value of Int is : $maxIntValue")
    println("Minimum value of Int is : $minIntValue")

    //BYTE
    val maxByteValue: Byte = Byte.MAX_VALUE
    val minByteValue: Byte = Byte.MIN_VALUE

    println("Maximum value of Byte is : $maxByteValue")
    println("Minimum value of Byte is : $minByteValue")

    //SHORT
    val maxShortValue: Short = Short.MAX_VALUE
    val minShortValue: Short = Short.MIN_VALUE

    println("Maximum value of Short is : $maxShortValue")
    println("Minimum value of Short is : $minShortValue")

    //LONG
    val maxLongValue: Long = Long.MAX_VALUE
    val minLongValue: Long = Long.MIN_VALUE

    println("Maximum value of Long is : $maxLongValue")
    println("Minimum value of Long is : $minLongValue")
}